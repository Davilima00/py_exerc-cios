# metade dobro almento

def metade(numero):
    """param numero a entrata de qualquer numero
    retun a saida da metade """
    return numero / 2


def dobro(numero):
    """param numero a entrada
    return a saida do dobro"""
    return numero * 2


def tripo(numero):
    """param numero a entrada
    return a saida do tripo"""
    return numero * 3


def almento(numero):
    """param numero a entrada
    return a saida com 10 pocento de almento"""
    return numero * (10/100)

